#Intersction of sets

setx = set(['Green','Blue'])
sety= set(['Red','Green','Yellow'])
setz= setx & sety
print(setz)

#union of sets

setx = set(['Green','Blue'])
sety= set(['Red','Green','Yellow'])
setz= setx | sety
print(setz)

#symmetric difference

setx = set(['Green','Blue'])
sety= set(['Red','Green','Yellow'])
setz= setx ^ sety
print(setz)
